package com.mThanuj.LMS.helpers;

public class Book {

    String name;

    String author;

    String content;

    int pages;


    public Book() {
    }

    public Book(String name, String author, String content, int pages) {
        this.name = name;
        this.author = author;
        this.content = content;
        this.pages = pages;
    }

}
